// The Hashgraph Online Standards SDK for Go is the official Go implementation
// of the Hiero Consensus Specifications (HCS). It provides a comprehensive
// set of packages for interacting with the Hedera Consensus Service,
// inscribing data to the Hedera public ledger, and integrating with the
// Hashgraph Online Registry Broker.
//
// # Standards Implemented
//
// The SDK implements the following HCS standards:
//
//   - HCS-2: Topic Registry management (https://hol.org/docs/standards/hcs-2)
//   - HCS-5: Hashinal NFT minting (https://hol.org/docs/standards/hcs-5)
//   - HCS-11: Profile Metadata (https://hol.org/docs/standards/hcs-11)
//   - HCS-14: Universal Agent ID (https://hol.org/docs/standards/hcs-14)
//   - HCS-15: Base/Petal Accounts (https://hol.org/docs/standards/hcs-15)
//   - HCS-16: Flora (multi-party consensus) (https://hol.org/docs/standards/hcs-16)
//   - HCS-17: State Hash verification (https://hol.org/docs/standards/hcs-17)
//   - HCS-27: Checkpoint & Merkle proofs (https://hol.org/docs/standards/hcs-27)
//
// # Documentation
//
// Full SDK documentation: https://hol.org/docs/libraries/standards-sdk/
//
// Hiero Consensus Specifications: https://hol.org/docs/standards
//
// Hashgraph Online ecosystem: https://hol.org
//
